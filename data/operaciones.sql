CREATE TABLE OPERACIONES (
	ID   INTEGER PRIMARY KEY AUTOINCREMENT,
	FH   TEXT, 
	TIPO VARCHAR(1) CHECK (TIPO IN ('*','+')),
	NR   INT,
	DR   INT, 
	N1   INT, 
 	D1   INT, 
 	N2   INT,
	D2   INT
);

CREATE TABLE OPERANDOS (
	F     TEXT NOT NULL,
	IDOP  INT,  
	TIPO  INT  NOT NULL,
	FOREIGN KEY(IDOP) REFERENCES OPERACIONES(ID),
	FOREIGN KEY(TIPO) REFERENCES OPERANDOSTIPO(ID),
	PRIMARY KEY (F, IDOP, TIPO)
);

CREATE TABLE OPERANDOSTIPO (
	ID INTEGER PRIMARY KEY,
	TIPO TEXT  NOT NULL
);

